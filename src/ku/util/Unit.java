package ku.util;

/**
 * Represent a unit.
 * @author Nitith Chayakul
 * @version 1.0
 */
public interface Unit {
	
	/**
	 * Return a value of a unit.
	 * @return value of a unit
	 */
	public double getValue() ;
	
	/**
	 * Return name of a unit.
	 * @return name of a unit
	 */
	public String toString() ;
}
